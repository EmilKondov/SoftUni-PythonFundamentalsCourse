given_string = input().split("|") # прочитаме елементите и ги разделяме в лист по елемент "|"

sub_string = []   # създаваме си празен лист, в който ще добавяме новите елементи.

for element in given_string[::-1]: # започваме итерация от в обратен ред от последния елемент
    sub_string.extend(element.split()) # раздели последния елемент ["7 88"] по разстояние -> ["7", "88"] и го добави в  sub_string

print(*sub_string) # разпакетирай всички елементи от sub_string